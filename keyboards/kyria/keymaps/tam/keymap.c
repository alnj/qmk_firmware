// TAP DANCE /////////////////////////////////////////////////////////////////
// CACV: key that C-A when held, C-C when tapped, and C-V when double tapped
enum {
    CACV = 0
};
typedef enum { // Tap dance states
  SINGLE_TAP,
  SINGLE_HOLD,
  DOUBLE_SINGLE_TAP
} td_state_t;
static td_state_t td_state; // create a global instance of the tapdance state type
int cur_dance (qk_tap_dance_state_t *state); // function to track the current tapdance state
void CA_CC_CV_finished (qk_tap_dance_state_t *state, void *user_data); // `finished` function for each tapdance keycode
int cur_dance (qk_tap_dance_state_t *state) { // track the tapdance state to return
  if (state->count == 1) {
    if (state->interrupted || !state->pressed) {
        return SINGLE_TAP;
    } else {
        return SINGLE_HOLD;
    }
  }
  if (state->count == 2) {
      return DOUBLE_SINGLE_TAP;
  }
  else {
      return 3; // any number higher than the maximum state value you return above
  }
}
void CA_CC_CV_finished(qk_tap_dance_state_t *state, void *user_data) { // handle the possible states for each tapdance keycode you define:
  td_state = cur_dance(state);
  switch (td_state) {
    case SINGLE_TAP:
      tap_code16(KC_COPY);
      break;
    case SINGLE_HOLD:
      tap_code16(C(KC_A));
      break;
    case DOUBLE_SINGLE_TAP: 
      tap_code16(KC_PSTE);
  }
}
